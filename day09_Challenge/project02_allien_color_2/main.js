// *Question 26:* Alien Colors #2: Choose a color for an alien, 
// then write an if-else chain.
var alienColor = "green";
if (alienColor == "green") {
    console.log("You just earned 10 points for shooting the alien!");
}
else {
    console.log("You just earned 5 points.");
}
alienColor = "yellow";
if (alienColor == "green") {
    console.log("You just earned 5 points for shooting the alien!");
}
else {
    console.log("You just earned 2 points.");
}
